drop DATABASE getgo;

CREATE database Getgo;
USE Getgo;

/* tables creation */

CREATE table `course`(
	`course_id` int(7) primary key auto_increment,
	`course_name` varchar(100),
	`credits` varchar(10),
	`decription` varchar(100)
);

CREATE table `instituition`(
	`university_id` int(7) primary key auto_increment,
	`school_name` varchar(40),
	`city` varchar(40),
	`province` varchar(10),
	`country` varchar(80),
	`postal_code` varchar(10),
	`type` varchar(10),
	`description` varchar(100)
);


CREATE table `department`(
	`department_id` int(7) primary key auto_increment,
	`university_id` int(7),
	`department_name` varchar(40),
	`average_mark` int(3),
	`description` varchar(100),
	INDEX(`university_id`),
	FOREIGN KEY (`university_id`)
	REFERENCES `instituition`(`university_id`)
);

CREATE table `major`(
	`major_id` int(7) primary key auto_increment,
	`department_id` int(7),
	`university_id` int(7),
	`name` varchar(40),
	`description` varchar(100),
	INDEX(`department_id`),
	FOREIGN KEY (`department_id`)
	REFERENCES `department`(`department_id`),

	INDEX(`university_id`),
	FOREIGN KEY (`university_id`)
	REFERENCES `instituition`(`university_id`)

);

CREATE table `group`(
	`course_id` int(7),
	`university_id` int(10),
	`group` varchar(8),
	INDEX(`course_id`),
	FOREIGN KEY (`course_id`)
	REFERENCES `course`(`course_id`)

);


	/* format for user_id is 00000 (5 digits) */
CREATE table `condition`(
	`condition_id` int(10) primary key auto_increment,
	`university_id` int(10) not null,
	`department_id` int(10) not null,
	`min_mark` int(5) not null,
    `condition` varchar(80),
    INDEX (`university_id`), 
    FOREIGN KEY (`university_id`)
    REFERENCES `instituition`(`university_id`),

    INDEX (`department_id`),
    FOREIGN KEY (`department_id`)
    REFERENCES `department`(`department_id`)
);


CREATE table profile(
	`student_id` int(5) primary key auto_increment,
	`first_name` varchar(40),
	`last_name` varchar(40),
	`email` varchar(80),
	`city` varchar(40),
	`province` varchar(10),
	`country` varchar(10),
	`postal_code` varchar(10),
	`international` int(1),
	`password` varchar(40),
	`highestEducation` varchar(30),

	INDEX (`student_id`),
	FOREIGN KEY (`student_id`)
	REFERENCES profile(`student_id`)
);


CREATE table profile_marks(
	`student_id` int(5),
	`course_id` int(7),
	`mark` int(4),


	INDEX (`student_id`),
	FOREIGN KEY (`student_id`)
	REFERENCES `profile`(`student_id`),

	INDEX (`course_id`),
	FOREIGN KEY (`course_id`)
	REFERENCES `group`(`course_id`)
);


